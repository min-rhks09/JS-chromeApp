const calculator = {
    add: function(a, b){
        console.log(a + b);
    },
    minus: function(a, b){
        console.log(a - b);
    },
    divide: function(a, b){
        console.log(a / b);
    },
    power: function(a, b){
        console.log(a ** b);
    },
};

calculator.add(1, 2);

calculator.minus(3, 2);

calculator.divide(2, 2);

calculator.power(2, 2);

